const portfolioShell = (
  partial: Pick<
    CustomerPortfolio,
    | "id"
    | "nr"
    | "companyName"
    | "registrationNumber"
    | "contractNo"
    | "period"
    | "scope"
    | "feeEur"
    | "emailForInvoices"
    | "clientContactName"
    | "clientContactPosition"
    | "sanctionsCheck"
    | "address"
    | "infoRequestDeadline"
    | "reportDraft"
    | "finalReport"
  > &
    Partial<Pick<CustomerPortfolio, "firstInvoiceAmountEur" | "secondInvoiceAmountEur" | "thirdInvoiceAmountEur">>,
): CustomerPortfolio => {
  const fee = partial.feeEur ?? 0;
  const q1 = Math.round(fee * 0.25);
  const q2 = Math.round(fee * 0.5);
  const q3 = fee - q1 - q2;
  const rowIdx = Math.max(
    0,
    Math.min((partial.nr ?? 1) - 1, DEMO_CUSTOMER_KYC.length - 1),
  );
  const kycRow = DEMO_CUSTOMER_KYC[rowIdx] ?? {};

  const merged: CustomerPortfolio = {
    lursoft: null,
    industry: null,
    naceCode: null,
    kycForm: null,
    idCopy: null,
    meetingDate: null,
    clientAcceptanceComplete: null,
    riskLevel: null,
    contractSigned: null,
    contractDate: null,
    additionalInvoicing: null,
    invoiceCheck: 0,
    contactEmail: null,
    whatsapp: null,
    lastContactDate: null,
    language: "English",
    framework: "Applicable statutory requirements",
    ...kycRow,
    ...partial,
    firstInvoicePercent: "25%",
    firstInvoiceAmountEur: partial.firstInvoiceAmountEur ?? q1,
    secondInvoicePercent: "50%",
    secondInvoiceAmountEur: partial.secondInvoiceAmountEur ?? q2,
    thirdInvoicePercent: "25%",
    thirdInvoiceAmountEur: partial.thirdInvoiceAmountEur ?? q3,
    sanctionsCheck:
      partial.sanctionsCheck ?? partial.companyName ?? null,
  };

  const invSum =
    (merged.firstInvoiceAmountEur ?? 0) +
    (merged.secondInvoiceAmountEur ?? 0) +
    (merged.thirdInvoiceAmountEur ?? 0);
  merged.invoiceCheck = invSum === (merged.feeEur ?? 0) ? merged.feeEur ?? 0 : 0;

  return merged;
};

